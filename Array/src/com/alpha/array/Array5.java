package com.alpha.array;

public class Array5
{
	public static void main(String[] args)
	{
		char ch[] = {'a','e','i','w','u'};
		for (int i = 0; i < ch.length; i++) 
		{
			if (ch[i] =='a'||ch[i]=='e'||ch[i]=='i'||ch[i]=='o'||ch[i]=='u')
		{
			System.out.println(ch[i]);
		}
		}
	}
}
		

