package com.alpha.wrapper.session45;

public class AutoBoxing {

	public static void main(String[] args) {
		
		//auto-boxing
		int i = 20;
		Integer obj1=i;
		System.out.println(obj1);
		
		//auto-unboxing
		
		Integer obj2=30;
		int i1 = obj2;
		System.out.println(i1);
		
	}

}
